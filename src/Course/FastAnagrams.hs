-- Return all anagrams of the given string
-- that appear in the given dictionary file.
fastAnagrams ::
  Chars
  -> Filename
  -> IO (List Chars)
fastAnagrams s dictname = let perms = S.fromList . hlist $ permutations s in
  filter (`S.member` perms) <$> lines <$> readFile dictname
